using BlankUIFrameWork;

namespace BlankFramework
{
    public class UIWindow
    {
        /// <summary>
        /// 每个场景只能执行一次
        /// </summary>
        /// <param name="viewController"></param>
        /// <returns></returns>
        public static void InitWithRootViewController(UIViewController viewController)
        {
            UINavigationController.Instance = new UINavigationController();
            UIRoot.Instance.WindowRoot.AddChild(viewController);
        }
    }
}