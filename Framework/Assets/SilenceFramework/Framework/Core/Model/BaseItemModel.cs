namespace BlankFramework
{
    /// <summary>
    /// 需要持久化数据/缓存数据的类的基类.类序列化为字典
    /// </summary>
    public abstract class BaseItemModel<T> : IBaseItemModel<T> where T : IBaseItemModel<T>
    {
        public string ID { get; set; }
        public abstract string ModelToJsonString();
        public abstract T JsonToModelString(TinyJson.Node jsonNode);
        public override string ToString()
        {
            return this.ModelToJsonString();
        }
    }
}